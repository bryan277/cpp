#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << pow(3,3) << endl;
    //27
    cout << sqrt(36) << endl;
    //6
    cout << round(4.6) << endl;
    //5
    cout << ceil(4.1) << endl;
    //5
    cout << floor(4.8) << endl;
    //4
    cout << fmax(3, 10) << endl;
    //10
    cout << fmin(3, 10) << endl;
    //3
}
